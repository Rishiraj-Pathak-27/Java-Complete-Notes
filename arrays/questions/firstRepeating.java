// Return the First Repeating Element

public class firstRepeating {
    
    public static int repeater(int[] arr) {
        int n = arr.length;
        int count = 0;
        for(int i=0; i<n; i++) {
            count = arr[i];
        }
    }
    public static void main(String []args) {
        int[] arr = {2,5,1,5,3,2};
    }
}